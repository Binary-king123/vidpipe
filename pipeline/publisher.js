/**
 * Publish a video to the database
 * @param {object} opts
 * @param {string} opts.title
 * @param {string} opts.description - can be empty (auto-generated)
 * @param {string} opts.filePath - relative web path e.g. /videos/001/000001.mp4
 * @param {string} opts.thumbnail - relative web path e.g. /videos/001/000001.jpg
 * @param {string} opts.category
 * @param {string[]} opts.tags
 * @param {string} opts.cdnUrl - original queue URL to mark as done
 */
async function publishVideo({ title, description, filePath, thumbnail, category, tags, cdnUrl }) {
  // Auto-generate description if empty
  const finalDesc = description && description.trim().length > 50
    ? description.trim()
    : autoGenerateDescription(title, category, tags);

  // Generate unique slug
  let slug = generateSlug(title);

  // Ensure slug uniqueness (retry up to 5 times)
  for (let i = 0; i < 5; i++) {
    const existing = await prisma.video.findUnique({ where: { slug } });
    if (!existing) break;
    slug = generateSlug(title); // regenerate with new suffix
  }

  const video = await prisma.video.create({
    data: {
      title: title.trim(),
      slug,
      filePath,
      thumbnail,
      description: finalDesc,
      category: category || 'desi',
      tags: tags.length > 0 ? tags : ['desi', 'indian'],
      views: 0,
    },
  });

  // Mark queue item as done
  if (cdnUrl) {
    await prisma.videoQueue.update({
      where: { cdnUrl },
      data: { status: 'done' },
    });
  }

  console.log(`\n  ✅ Published: "${video.title}"`);
  console.log(`     Slug:      ${video.slug}`);
  console.log(`     URL:       ${process.env.NEXT_PUBLIC_APP_URL || 'https://ilovedesi.fun'}/video/${video.slug}`);

  return video;
}

module.exports = { publishVideo, generateSlug, autoGenerateDescription };

