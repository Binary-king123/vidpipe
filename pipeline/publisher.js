/**
 * MODULE 6 — PUBLISHER
 * Saves processed video metadata to DB and marks queue item as done.
 * Generates unique SEO slugs from titles.
 */

const { PrismaClient } = require('@prisma/client');
const path = require('path');
require('dotenv').config({ path: path.join(__dirname, '../.env') });

const prisma = new PrismaClient();

/**
 * Generate a URL-safe slug from a title with a short random suffix
 * e.g. "Hot Desi Girl Dance" → "hot-desi-girl-dance-a3f9"
 */
function generateSlug(title) {
  const base = title
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '')
    .trim()
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .substring(0, 60);

  const suffix = Math.random().toString(36).substring(2, 6);
  return `${base}-${suffix}`;
}

